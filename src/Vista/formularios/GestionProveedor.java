    private void BorrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BorrarMouseClicked
        if (datoSeleccionado >= 0) {
            String dato = String.valueOf(TablaProveedores.getValueAt(datoSeleccionado, 0));
            CRUDProveedor pro = new CRUDProveedor();
            Component rootPane = null;
            if (JOptionPane.showConfirmDialog(rootPane,
                    "Se eliminar√° el registro, ¬øDesea continuar?",
                    "Eliminar Registro",
                    JOptionPane.WARNING_MESSAGE,
                    JOptionPane.YES_NO_OPTION)
                    == JOptionPane.YES_OPTION) {
                pro.eliminar(dato);
                mostrar();
                JOptionPane.showMessageDialog(null,
                        "Dato eliminado correctamente");
            }
        } else {
            JOptionPane.showMessageDialog(null,
                    "Debe seleccionar un registro de la tabla");
        }
    }//GEN-LAST:event_BorrarMouseClicked

    private void EditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditarMouseClicked
       if (datoSeleccionado >= 0) {
           Proveedores prove = new Proveedores();
     prove.setSize(560, 970);
     prove.setLocation(0,0);
     
     Main.mainForm.removeAll();
     Main.mainForm.add(prove, BorderLayout.CENTER);
     Main.mainForm.revalidate();
     Main.mainForm.repaint();
            //mandar datos al formulario
            Proveedores.Ced.setText(String.valueOf(TablaProveedores.getValueAt(datoSeleccionado, 0)));
            Proveedores.Nombres.setText(String.valueOf(TablaProveedores.getValueAt(datoSeleccionado, 1)));
            Proveedores.Apellidos.setText(String.valueOf(TablaProveedores.getValueAt(datoSeleccionado, 2)));
            Proveedores.direc.setText(String.valueOf(TablaProveedores.getValueAt(datoSeleccionado, 3)));
            Proveedores.Telefono.setText(String.valueOf(TablaProveedores.getValueAt(datoSeleccionado, 4)));
            Proveedores.Correo.setText(String.valueOf(TablaProveedores.getValueAt(datoSeleccionado, 5)));
            Proveedores.Ced.enable(false);
            Proveedores.Guardar.setVisible(false);
            Proveedores.Actualizar.setVisible(true);
            prove.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro a actualizar");
        }
    }//GEN-LAST:event_EditarMouseClicked

