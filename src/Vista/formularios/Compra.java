   public void buscarProducto() {
        int id=Integer.parseInt(CodProducto.getText());
        if(CodProducto.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe ingresar el Código de Producto");
        }else{
            POJOProducto p=pro.listarID(id);
            if(p.getIDProducto()!=0){
                NombreProducto.setText(p.getNomProducto());
                Stock.setText(""+p.getCantProducto());
            }else{
                JOptionPane.showMessageDialog(this, "Producto no registrado");
                CodProducto.requestFocus();
            }
        }
    }
    
