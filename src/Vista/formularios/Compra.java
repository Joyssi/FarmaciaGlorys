     public void agregarProducto(){
        modelo=(DefaultTableModel)TablaProducto.getModel();
        int id=Integer.parseInt(CodProducto.getText().toString());
        String nom=NombreProducto.getText();
        cant=Integer.parseInt(Cantidad.getValue().toString());
        precio=Double.parseDouble(Precio.getText());
        int stock=Integer.parseInt(Stock.getText());
        total=cant*precio;
        ArrayList lista = new ArrayList();
        if(stock>0){
            lista.add(id);
            lista.add(nom);
            lista.add(cant);
            lista.add(precio);
            lista.add(total);
            Object[]ob=new Object[5];
            ob[0]=lista.get(0);
            ob[1]=lista.get(1);
            ob[2]=lista.get(2);
            ob[3]=lista.get(3);
            ob[4]=lista.get(4);
            modelo.addRow(ob);
            TablaProducto.setModel(modelo);
            calcularTotal();
        }else{
            JOptionPane.showMessageDialog(this, "Stock producto no disponible");
        }
    }
     
      public void calcularTotal() {
        totalpagar=0;
        for (int i = 0; i < TablaProducto.getRowCount(); i++) {
            cant=Integer.parseInt(TablaProducto.getValueAt(i, 2).toString());
            precio=Double.parseDouble(TablaProducto.getValueAt(i, 3).toString());
            totalpagar=totalpagar+(cant*precio);
        }
        totalPagar.setText(""+totalpagar);
    }
    
