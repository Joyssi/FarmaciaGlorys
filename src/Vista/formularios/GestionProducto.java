    private void EditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditarMouseClicked
         if (datoSeleccionado >= 0) {
             Producto pro = new Producto();
        Main.mainForm.removeAll();
        Main.mainForm.add(pro, BorderLayout.CENTER);
        Main.mainForm.revalidate();
        Main.mainForm.repaint();
            //mandar datos al formulario
            Producto.Nombre.setText(String.valueOf(TablaProductos.getValueAt(datoSeleccionado, 1)));
            Producto.Descripcion.setText(String.valueOf(TablaProductos.getValueAt(datoSeleccionado, 2)));
            Producto.Cantidad.setText(String.valueOf(TablaProductos.getValueAt(datoSeleccionado, 3)));
            Producto.Precio.setText(String.valueOf(TablaProductos.getValueAt(datoSeleccionado, 4)));
            Producto.Vencimiento.setText(String.valueOf(TablaProductos.getValueAt(datoSeleccionado, 5)));
            Producto.Guardar.setVisible(false);
            Producto.Actualizar.setVisible(true);
            pro.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro a actualizar");
        }
    }//GEN-LAST:event_EditarMouseClicked

    private void BorrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BorrarMouseClicked
        if (datoSeleccionado >= 0) {
            int codigo = Integer.parseInt((String) TablaProductos.getValueAt(datoSeleccionado, 0));
            CRUDProducto pro = new CRUDProducto();
            Component rootPane = null;
            if (JOptionPane.showConfirmDialog(rootPane,
                "Se eliminará el registro, ¿Desea continuar?",
                "Eliminar Registro",
                JOptionPane.WARNING_MESSAGE,
                JOptionPane.YES_NO_OPTION)
            == JOptionPane.YES_OPTION) {
            pro.eliminar(codigo);
            mostrar();
            JOptionPane.showMessageDialog(null,
                "Dato eliminado correctamente");
        }
        } else {
            JOptionPane.showMessageDialog(null,
                "Debe seleccionar un registro de la tabla");
        }
    }//GEN-LAST:event_BorrarMouseClicked

